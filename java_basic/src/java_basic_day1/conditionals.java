package java_basic_day1;

public class conditionals {

	public static void main(String[] args) {
		int sayi=20;
		if (sayi<20) {
			System.out.println("sayi 20 den küçüktür");
		}
		else if (sayi>20) {
			System.out.println("sayi 20 den büyüktür");
		}
		else {
			System.out.println("sayi 20 ye eşittir");
		}

	}

}
