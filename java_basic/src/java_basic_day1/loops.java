package java_basic_day1;

public class loops {

	public static void main(String[] args) {
		for (int i = 0; i <= 10; i+=2) {
			System.out.println(i);
		}
		System.out.println("for döngüsü bitti");
		
		int i=5;
		while (i<10) {
			System.out.println(i);
			i+=1;
		}
		System.out.println("while döngüsü bitti");

	}

}
